import Head from 'next/head';
import styles from './Index.module.scss';
import { GetStaticProps } from 'next';
import { Prisma } from '@prisma/client';

interface IndexProps {
  user: Prisma.UserGetPayload<{}>;
}

export default function Index({ user }: IndexProps) {
  console.log(user);

  return (
    <div className={styles.container}>
      <Head>
        <title>MyGymPal</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>MyGymPal</h1>
      </main>
    </div>
  );
}

export const getStaticProps: GetStaticProps = async () => {
  const {prisma} = await import('@/utils/prisma');

  let user = await prisma.user.findFirst({ where: { name: 'Ottigan' } });

  if (!user) {
    user =  await prisma.user.create({ data: { name: 'Ottigan', surname: 'Gottry' } });
  }

  return {
    props: {
      user,
    },
  };
};
